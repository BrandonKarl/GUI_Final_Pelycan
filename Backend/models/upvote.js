import mongoose from 'mongoose'

const upvoteSchema = new mongoose.Schema({ // What each user will look like
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User'
  },
  post: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Post'
  }
}, { timestamps: true })

export default mongoose.model('Upvote', upvoteSchema)